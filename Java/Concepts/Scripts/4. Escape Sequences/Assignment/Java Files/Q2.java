/* Q.2 WAP to convert inches to Foot. (Given is 61.8 inches) */

public class Q2
{
    public static void main(String args[])
    {
        float inches=61.8F;
        float foot=inches/12;
        System.out.print(inches+" Inches to Foot is: "+foot+" Foot");
    }
}

