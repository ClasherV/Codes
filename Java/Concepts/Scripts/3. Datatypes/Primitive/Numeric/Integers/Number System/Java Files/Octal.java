public class Octal
{
    public static void main(String[] args)
    {
        int a=037;
        int b=010;
        int c=011;
        System.out.println("Octal 037: "+a);    
        System.out.println("Octal 010: "+b);    
        System.out.println("Octal 011: "+c);    
    }
}

