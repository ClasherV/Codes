public class Binary
{
    public static void main(String[] args)
    {
        int a=0B1111;
        int b=0B010101;
        int c=0b1000;
        System.out.println("Binary 1111: "+a);
        System.out.println("Binary 010101: "+b);
        System.out.println("Binary 1000: "+c);
    }
}

// O/p: Binary 1111: 15
//      Binary 010101: 21
//      Binary 1000: 8