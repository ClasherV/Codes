public class Hexadecimal
{
    public static void main(String[] args)
    {
        int a=0X15;
        int b=0X16;
        int c=0x10;
        int d=0xAF;
        System.out.println("Hexadecimal 0X15: "+a);
        System.out.println("Hexadecimal 0X16: "+b);
        System.out.println("Hexadecimal 0x10: "+c);
        System.out.println("Hexadecimal AF: "+d);
    }
}

